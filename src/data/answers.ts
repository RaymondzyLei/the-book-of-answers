const lists: Record<Category, string[]> = {
  affirmative,
  negative,
  neutral,
  action,
  mysterious,
};

const weights: Record<Category, number> = {
  affirmative: 0.15,
  negative: 0.15,
  neutral: 0.3,
  action: 0.3,
  mysterious: 0.1,
};

export function pickRandomAnswer(customWeights?: Record<Category, number>): string {
  const w = customWeights ?? weights;
  const r = Math.random();
  let acc = 0;
  for (const category of Object.keys(w) as Category[]) {
    acc += w[category];
    if (r <= acc) {
      const pool = lists[category];
      return pool[Math.floor(Math.random() * pool.length)];
    }
  }
  const fallback = lists.neutral;
  return fallback[Math.floor(Math.random() * fallback.length)];
}
